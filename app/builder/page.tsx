function makeAgentAdvice(prompt: string) {
  const text = prompt.toLowerCase();

  if (!prompt.trim()) {
    return "Enter a prompt. Example: Create a premium dashboard for ArcheNova Intelligence Platform.";
  }

  if (text.includes("dashboard")) {
    return "Agent: Use a hero summary, metric cards, signal list, and decision section. Dashboard outputs should prioritize clarity and hierarchy.";
  }

  if (text.includes("landing") || text.includes("home")) {
    return "Agent: Use a strong hero, concise mission, visual product cards, and a clear action path.";
  }

  if (text.includes("system") || text.includes("architecture")) {
    return "Agent: Separate the design into input, intelligence, generation, execution, and feedback layers.";
  }

  return "Agent: Good prompt. Next, refine the desired user, purpose, page type, and final action.";
}

